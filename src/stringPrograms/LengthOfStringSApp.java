package stringPrograms;

public class LengthOfStringSApp {
	public static String length(String s1,String s2)
	{
		int s3=s1.length();
		int s4=s2.length();
		if(s3==s4)
		{
			System.out.println("both string lengths are equal");
		}
		else
		{
			System.out.println("both string lengths are not equal");
		}
		return s2;
		
	}

}
